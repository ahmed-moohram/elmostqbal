/**
 * مخطط قاعدة البيانات وعلاقات البيانات لمنصة EduFutura
 * 
 * يوضح هذا الملف الهيكل العام لقاعدة البيانات وكيفية ترابط الكائنات معًا
 */

/**
 * نموذج المستخدم (User)
 * - يمثل جميع المستخدمين في النظام (الطلاب، المعلمين، المدراء)
 * - يحتوي على بيانات الحساب الأساسية وتفاصيل المستخدم
 * 
 * العلاقات:
 * - مع نموذج Student (1:1) إذا كان الدور "student"
 * - مع نموذج Teacher (1:1) إذا كان الدور "teacher"
 * - مع نموذج Courses (n:m) من خلال الاشتراك / الإنشاء
 */

/**
 * نموذج الطالب (Student)
 * - يحتوي على بيانات تخص الطالب
 * - مرتبط بمستخدم واحد فقط
 * 
 * العلاقات:
 * - مع نموذج User (1:1)
 * - مع نموذج Course (n:m) من خلال الاشتراك في الدورات
 * - مع نموذج Lesson (n:m) من خلال التقدم والإنجاز
 */

/**
 * نموذج المعلم (Teacher)
 * - يحتوي على بيانات تخص المعلم
 * - مرتبط بمستخدم واحد فقط
 * 
 * العلاقات:
 * - مع نموذج User (1:1)
 * - مع نموذج Course (1:n) كمنشئ للدورات
 */

/**
 * نموذج الدورة التعليمية (Course)
 * - يمثل دورة تعليمية كاملة
 * - يحتوي على بيانات الدورة وتفاصيلها
 * 
 * العلاقات:
 * - مع نموذج Teacher (n:1) منشئ الدورة
 * - مع نموذج Category (n:1) تصنيف الدورة
 * - مع نموذج Lesson (1:n) الدروس المكونة للدورة
 * - مع نموذج Student (n:m) الطلاب المشتركين
 */

/**
 * نموذج الدرس (Lesson)
 * - يمثل درسًا واحدًا في دورة تعليمية
 * - يحتوي على محتوى الدرس ومرفقاته
 * 
 * العلاقات:
 * - مع نموذج Course (n:1) الدورة التي ينتمي إليها
 * - مع نموذج Quiz (1:1) اختبار الدرس إن وجد
 * - مع نموذج Resource (1:n) موارد الدرس
 */

/**
 * نموذج فئة الدورة (Category)
 * - يمثل تصنيف للدورات
 * 
 * العلاقات:
 * - مع نموذج Course (1:n) الدورات التي تنتمي للفئة
 */

/**
 * نموذج الاختبار (Quiz)
 * - يمثل اختبارًا مرتبطًا بدرس
 * - يحتوي على الأسئلة والإجابات
 * 
 * العلاقات:
 * - مع نموذج Lesson (1:1) الدرس المرتبط بالاختبار
 * - مع نموذج Question (1:n) أسئلة الاختبار
 */

/**
 * نموذج المرفقات (Resource)
 * - يمثل مرفقات إضافية للدرس
 * 
 * العلاقات:
 * - مع نموذج Lesson (n:1) الدرس المرتبط
 */

/**
 * نموذج الكتاب (Book)
 * - يمثل كتابًا أو مذكرة يمكن شراؤها
 * 
 * العلاقات:
 * - مع نموذج Teacher (n:1) المعلم مؤلف الكتاب
 * - مع نموذج User (n:m) المستخدمين الذين اشتروا الكتاب
 */

/**
 * مخطط قاعدة البيانات - العلاقات
 * 
 * User (1:1) -----> Student
 *      |
 *      | (1:1)
 *      v
 * Teacher (1:n) ----> Course (1:n) ----> Lesson (1:1) ----> Quiz
 *                       ^                    |
 *                       |                    | (1:n)
 *                       |                    v
 * Category (1:n) --------                 Resource
 * 
 * Student (n:m) <-----> Course
 * User (n:m) <-----> Book <---- (n:1) Teacher
 */

